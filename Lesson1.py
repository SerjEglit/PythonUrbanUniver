# 1st program
# Задача 1: Арифметика
# Возводим 9 в степень 0.5 (квадратный корень), затем умножаем результат на 5.
result_task1 = (9 ** 0.5) * 5
print(result_task1)  # Предполагаемый результат: 15.0

