# 3rd program
# Задача 3: Школьная загадка
# Выражение 2 * 2 + 2 без приоритета (происходит сначала умножение, потом сложение)
result_no_priority = 2 * 2 + 2

# Выражение 2 * (2 + 2) с приоритетом для сложения (используем скобки)
result_with_priority = 2 * (2 + 2)

# Сравниваем два результата
comparison_result = result_no_priority == result_with_priority

# Выводим результаты
print(result_no_priority)  # Предполагаемый результат: 6
print(result_with_priority)  # Предполагаемый результат: 8
print(comparison_result)  # Предполагаемый результат: False

